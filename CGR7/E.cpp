const int N = 3e5+10;

struct FT {
	vector<ll> s;
	FT(int n) : s(n) {}
	void update(int pos, ll dif) { // a[pos] += dif
		for (; pos < sz(s); pos |= pos + 1) s[pos] += dif;
	}
	ll query(int pos) { // sum of values in [0, pos)
		ll res = 0;
		for (; pos > 0; pos &= pos - 1) res += s[pos-1];
		return res;
	}
	int lower_bound(ll sum) {// min pos st sum of [0, pos] >= sum
		// Returns n if no sum is >= sum, or -1 if empty sum is.
		if (sum <= 0) return -1;
		int pos = 0;
		for (int pw = 1 << 25; pw; pw >>= 1) {
			if (pos + pw <= sz(s) && s[pos + pw-1] < sum)
				pos += pw, sum -= s[pos-1];
		}
		return pos;
	}
};

int n, p[N], q[N], id[N], cur;
vector<int> ans;

bool find_right_bomb(FT& f, int x) {
	int fid = f.lower_bound(f.query(id[x])+1);
	cout << x << ' ' << fid << endl;
	if(fid == n) return false;
	else {
		f.update(fid, -1);
		return true;
	}
}

void find_next(FT& f) {
	while(find_right_bomb(f, cur)) --cur;
	ans.push_back(cur);
}

signed main() {
    ios::sync_with_stdio(false);
    //cout << fixed << setprecision(7);
    cin >> n;
    FT f(n);
    REP(i, n) {
    	cin >> p[i];
    	id[p[i]] = i;
    }
    ans.push_back(n);
    cur = n;
    REP(i, n) {
    	cin >> q[i];
    	f.update(q[i]-1, 1);
    	if(i != n-1) find_next(f);
    }
    for(int x : ans) cout << x << ' ';
    cout << endl;

    return 0;
}

